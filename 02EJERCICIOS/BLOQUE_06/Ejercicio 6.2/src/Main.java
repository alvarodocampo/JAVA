/*6.2  Imprime los números del 10 al 1 usando un while.*/

public class Main {
    public static void main (String[] args){

        System.out.println("Vamos a imprimir los número del 10 al 1");

       int contador = 10;
       while (contador>=1){
           System.out.println(contador);
           contador--;
       }
    }

}